export function translate(lang, key, vars) {
  const path = key.split(".");
  const lookup = (dict) => path.reduce((acc, part) => (acc == null ? acc : acc[part]), dict);

  let value = lookup(translations[lang]);
  if (value === undefined) value = lookup(translations.en);
  if (typeof value !== "string") return key;

  if (!vars) return value;
  return Object.entries(vars).reduce((str, [k, v]) => str.split(`{${k}}`).join(v), value);
}
